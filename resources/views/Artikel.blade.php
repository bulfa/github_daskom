        <p>Pengenalan Laravel :
Laravel adalah sebuah kerangka kerja (Framework) pemrograman PHP dengan
kode terbuka (Open Source) dengan konsep desain Model View Controller(MVC)
yang digunakan untuk mengembangkan sistem informasi berbasis website.
Tujuan utama pengembangan framework laravel ini adalah untuk membantu
developer dalam mengerjakan aplikasi agar waktu pengerjaan lebih cepat ketimbang
membangun sebuah kode dari awal. Laravel memiliki beberapa fitur unggulan yang
dapat dimanfaatkan oleh pengembang web dalam membangun sebuah web yang
dapat mempermudah dalam melakukan pengembangan aplikasi.
Fitur Unggulan Laravel :
Laravel memiliki beberapa fitur unggulan yang dapat dimanfaatkan oleh
pengembang web dalam membangun sebuah web, antara lain:
● Bundles. Sebuah fitur dengan sistem pengemasan modular dan tersedia
beragam di aplikasi.
● Eloquent ORM. Penerapan PHP lanjutan menyediakan metode internal dari
pola active record yang mengatasi masalah pada hubungan objek basis data.
● Application Logic. Merupakan bagian dari aplikasi menggunakan controller
atau bagian Route.
● Reverse Routing. Mendefinisikan relasi atau hubungan antara Link dan Route.
● Restful Controllers. Memisahkan logika dalam melayani HTTP GET dan
POST.
● Class Auto Loading. Menyediakan loading otomatis untuk class PHP.
● View Composer. Kode unit logikal yang dapat dieksekusi ketika view sedang
loading.
● IoC Container. Memungkinkan objek baru dihasilkan dengan pembalikan
controller. Migration. Penyedia sistem control untuk skema basis data.
● Unit Testing. Banyak tes untuk mendeteksi dan mencegah regresi.
● Automatic Pagination. Menyederhanakan tugas dari penerapan halaman.
